#include "fdf.h"

void	free_map(t_map *map)
{
	int	i;

	i = 0;
	while (i < map->nb_l)
	{
		free(map->int_m[i]);
		free(map->color_m[i]);
		i++;
	}
	free(map->color_m);
	free(map->int_m);
}
